package org.apache.polaris.persistence.nosql.coretypes.catalog;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.google.errorprone.annotations.CanIgnoreReturnValue;
import java.util.Optional;
import org.apache.polaris.core.storage.PolarisStorageConfigurationInfo;
import org.apache.polaris.persistence.nosql.coretypes.ObjBase;

/** Objects that hold storage configuration information. */
public interface CatalogStorageObj extends ObjBase {

  @JsonInclude(JsonInclude.Include.NON_ABSENT)
  Optional<PolarisStorageConfigurationInfo> storageConfigurationInfo();

  @JsonInclude(JsonInclude.Include.NON_ABSENT)
  Optional<String> storageIntegrationIdentifier();

  interface Builder<O extends CatalogStorageObj, B extends Builder<O, B>>
      extends ObjBase.Builder<O, B> {

    @CanIgnoreReturnValue
    B from(CatalogStorageObj from);

    @CanIgnoreReturnValue
    B storageConfigurationInfo(PolarisStorageConfigurationInfo storageConfigurationInfo);

    @CanIgnoreReturnValue
    B storageConfigurationInfo(
        Optional<? extends PolarisStorageConfigurationInfo> storageConfigurationInfo);

    @CanIgnoreReturnValue
    B storageIntegrationIdentifier(String storageIntegrationIdentifier);

    @CanIgnoreReturnValue
    B storageIntegrationIdentifier(Optional<String> storageIntegrationIdentifier);
  }
}
