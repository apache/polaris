package org.apache.polaris.test.azurite;

import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Target({ElementType.FIELD, ElementType.PARAMETER})
@Retention(RetentionPolicy.RUNTIME)
@Inherited
public @interface Azurite {
  /** Optional, use this file-system/storage-container name instead of a random one. */
  String storageContainer() default DEFAULT;

  String account() default DEFAULT;

  String secret() default DEFAULT;

  String DEFAULT = "azurite_default_value__";
}
