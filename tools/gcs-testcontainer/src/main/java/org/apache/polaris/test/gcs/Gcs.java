package org.apache.polaris.test.gcs;

import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Target({ElementType.FIELD, ElementType.PARAMETER})
@Retention(RetentionPolicy.RUNTIME)
@Inherited
public @interface Gcs {
  /** Optional, use this project ID instead of a random one. */
  String projectId() default DEFAULT;

  /** Optional, use this OAuth 2 token instead of a random one. */
  String oauth2token() default DEFAULT;

  /** Optional, use this bucket instead of a random one. */
  String bucket() default DEFAULT;

  String DEFAULT = "gcs_default_value__";
}
